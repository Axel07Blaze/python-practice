def calci (a, b):
    add = (a+b)
    div = (a/b)
    mul = (a*b)
    sub = (a-b)
    return add,div,mul,sub
    

addition,division,multiplication,subtraction = calci(20,30)

print("addition:",addition)
print("division:",division)
print("multiplication:",multiplication)
print("subtraction:",subtraction)



          
